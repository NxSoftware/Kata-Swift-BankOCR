    let entry = [" _  _  _  _  _  _  _  _  _ ",
                 "| || || || || || || || || |",
                 "|_||_||_||_||_||_||_||_||_|",
                 ""]
    XCTAssertEqual("000000000", EntryReader.read(entry), "All zeroes should return 000000000")
  }

